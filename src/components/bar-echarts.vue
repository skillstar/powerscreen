<template>
  <!--Echarts的容器 -->
  <div ref="divRefbar" :style="{ width: width, height: height }"></div>
</template>

<script setup>
import { ref, onMounted, watch } from 'vue'
import * as echarts from 'echarts'
import useEchart from '@/hooks/useEchart'

const props = defineProps({
  width: {
    type: String,
    default: '100%'
  },
  height: {
    type: String,
    default: '100%'
  },
  echartDatas: {
    type: Array,
    default: function () {
      return []
    }
  }
})

//监听 echartDatas 数据的变化
watch(
  () => props.echartDatas,
  (newV, oldV) => {
    setUpEchart(newV)
  }
)
let divRefbar = ref(null)
let myChart = null
onMounted(() => {
  setUpEchart(props.echartDatas) //第一次走这里
})

function setUpEchart(echartsdata = []) {
  if (!myChart) {
    myChart = useEchart(divRefbar.value)
  }
  myChart.setOption(getOption(echartsdata))
}

function getOption(echartDatas = []) {
  const category = echartDatas.map((item) => {
    return item.name
  })
  const categoryData = echartDatas.map((item) => {
    return item.value
  })
  const option = {
    grid: {
      left: '5%',
      right: '5%',
      top: '30%',
      bottom: '5%',
      containLabel: true // grid 区域是否包含坐标轴的刻度标签
    },
    tooltip: {},
    xAxis: {
      axisLine: {
        show: true,
        lineStyle: {
          color: '#42A4FF'
        }
      },
      axisTick: {
        show: false
      },
      axisLabel: {
        color: 'white'
      },

      data: category
    },
    yAxis: {
      name: '个',
      nameTextStyle: {
        color: 'white',
        fontSize: 13
      },
      axisLine: {
        show: true,
        lineStyle: {
          color: '#42A4FF'
        }
      },
      axisTick: {
        show: false
      },
      splitLine: {
        show: true,
        lineStyle: {
          color: '#42A4FF'
        }
      },
      axisLabel: {
        color: 'white'
      }
    },
    series: [
      {
        name: '销量',
        type: 'bar',
        barWidth: 17,
        itemStyle: {
          color: {
            type: 'linear',
            x: 0,
            y: 0,
            x2: 0,
            y2: 1,
            colorStops: [
              {
                offset: 0,
                color: '#01B1FF' // 0% 处的颜色
              },
              {
                offset: 1,
                color: '#033BFF' // 100% 处的颜色
              }
            ],
            global: false // 缺省为 false
          }
        },
        data: categoryData
      }
    ]
  }
  return option
}
</script>
<style scoped></style>
